# Se crea una variable llamada 'nombre' que guarda una cadena de texto
nombre = "julian velez G"

# print(nombre[2:5]) imprime una parte (subcadena) del texto original.
# En Python, los índices comienzan desde 0, así que:
# nombre[2:5] significa: toma los caracteres desde la posición 2 hasta la 4.
# (El índice final 5 NO se incluye).
# Entonces, la cadena es:  j (0)  u (1)  l (2)  i (3)  a (4)  n (5)
# Por tanto, se imprime: "lia"

print(nombre[2:5])  # Salida: lia
